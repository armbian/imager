import { load } from '@tauri-apps/plugin-store';

const AUTOCONFIG_STORE = '.autoconfig.dat';
let storeInstance: Awaited<ReturnType<typeof load>> | null = null;

async function getStore() {
  if (!storeInstance) {
    storeInstance = await load(AUTOCONFIG_STORE, { autoSave: true, defaults: {} });
  }
  return storeInstance;
}

// Keys
const K_ENABLE = 'autoconfig.enable';
const K_WIFI_ENABLED = 'autoconfig.wifi.enabled';
const K_WIFI_SSID = 'autoconfig.wifi.ssid';
const K_WIFI_KEY = 'autoconfig.wifi.key';
const K_WIFI_CC = 'autoconfig.wifi.countrycode';
const K_USER_NAME = 'autoconfig.user.name';
const K_USER_PASS = 'autoconfig.user.password';
const K_USER_KEY = 'autoconfig.user.key';
const K_USER_SHELL = 'autoconfig.user.shell';
const K_USER_REALNAME = 'autoconfig.user.realname';
const K_ROOT_PASS = 'autoconfig.root.password';
const K_ROOT_KEY = 'autoconfig.root.key';
const K_LOCALE = 'autoconfig.system.locale';
const K_TIMEZONE = 'autoconfig.system.timezone';
const K_STATIC_IP_ENABLED = 'autoconfig.network.static.enabled';
const K_STATIC_IP = 'autoconfig.network.static.ip';
