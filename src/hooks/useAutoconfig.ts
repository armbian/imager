export async function generateAutoconfigPayload(): Promise<string> {
  if (!(await getAutoconfigEnabled())) return '';

  const lines: string[] = [];
  lines.push('#/root/.not_logged_in_yet Generated by Armbian Imager');
  
  if (await getWifiEnabled()) {
    lines.push('PRESET_NET_CHANGE_DEFAULTS="1"');
    lines.push('PRESET_NET_ETHERNET_ENABLED="1"'); // Required per Armbian docs default
    lines.push('PRESET_NET_WIFI_ENABLED="1"');
    lines.push(`PRESET_NET_WIFI_SSID="${await getWifiSsid()}"`);
    lines.push(`PRESET_NET_WIFI_KEY="${await getWifiKey()}"`);
    lines.push(`PRESET_NET_WIFI_COUNTRYCODE="${await getWifiCountryCode()}"`);
    lines.push('PRESET_CONNECT_WIRELESS="y"');
  }

  if (await getStaticIpEnabled()) {
    lines.push('PRESET_NET_CHANGE_DEFAULTS="1"');
    lines.push('PRESET_NET_USE_STATIC="1"');
    lines.push(`PRESET_NET_STATIC_IP="${await getStaticIp()}"`);
    lines.push(`PRESET_NET_STATIC_MASK="${await getStaticMask()}"`);
    lines.push(`PRESET_NET_STATIC_GATEWAY="${await getStaticGateway()}"`);
    lines.push(`PRESET_NET_STATIC_DNS="${await getStaticDns()}"`);
  }

  const locale = await getLocale();
  if (locale) {
    lines.push('SET_LANG_BASED_ON_LOCATION="n"');
    lines.push(`PRESET_LOCALE="${locale}"`);
  }

  const tz = await getTimezone();
  if (tz) lines.push(`PRESET_TIMEZONE="${tz}"`);

  const rootpass = await getRootPassword();
  if (rootpass) lines.push(`PRESET_ROOT_PASSWORD="${rootpass}"`);

  const rootkey = await getRootKey();
  if (rootkey) lines.push(`PRESET_ROOT_KEY="${rootkey.trim()}"`);

  const user = await getUserName();
  const userpass = await getUserPassword();
  if (user && userpass) {
    lines.push(`PRESET_USER_NAME="${user}"`);
    lines.push(`PRESET_USER_PASSWORD="${userpass}"`);
    
    const userkey = await getUserKey();
    if (userkey) lines.push(`PRESET_USER_KEY="${userkey.trim()}"`);
    
    const userreal = await getUserRealName();
    if (userreal) lines.push(`PRESET_DEFAULT_REALNAME="${userreal}"`);
    
    const shell = await getUserShell();
    if (shell) lines.push(`PRESET_USER_SHELL="${shell}"`);
  }

  return lines.join('\n');
}
