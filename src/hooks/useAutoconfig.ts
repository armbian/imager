const K_STATIC_GATEWAY = 'autoconfig.network.static.gateway';
const K_STATIC_DNS = 'autoconfig.network.static.dns';

// Utility for getting strings/booleans
async function getBool(key: string, def = false): Promise<boolean> {
  const store = await getStore();
  const val = await store.get<boolean>(key);
  return val ?? def;
}
async function setBool(key: string, val: boolean) {
  const store = await getStore();
  await store.set(key, val);
  await store.save();
}
async function getString(key: string, def = ''): Promise<string> {
  const store = await getStore();
  const val = await store.get<string>(key);
  return val ?? def;
}
async function setString(key: string, val: string) {
  const store = await getStore();
  await store.set(key, val);
  await store.save();
}

